class SSD1306Display:
    """
    Minimal SSD1306 128x32 I2C display driver with 5x7 text rendering.
    """

    def __init__(self, bus: int = 1, addr: int = 0x3C):
        self.bus = smbus.SMBus(bus)
        self.addr = addr
        self.width = 128
        self.height = 32
        self.pages = self.height // 8
        self.buf = bytearray(self.width * self.pages)

    # --------------------------------------------------------
    # low-level I2C
    # --------------------------------------------------------

    def _cmd(self, *cmds: int) -> None:
        data = list(cmds)
        for i in range(0, len(data), 32):
            self.bus.write_i2c_block_data(self.addr, 0x00, data[i:i + 32])

    def _data(self, data_bytes: bytes | bytearray) -> None:
        for i in range(0, len(data_bytes), 32):
            self.bus.write_i2c_block_data(self.addr, 0x40, list(data_bytes[i:i + 32]))

    # --------------------------------------------------------
    # display lifecycle
    # --------------------------------------------------------

    def init(self) -> None:
        self._cmd(
            0xAE,             # display off
            0x20, 0x00,       # horizontal addressing mode
            0xB0,             # page start
            0xC8,             # COM scan direction remapped
            0x00, 0x10,       # low/high column start
            0x40,             # start line = 0
            0x81, 0x7F,       # contrast
            0xA1,             # segment remap
            0xA6,             # normal display
            0xA8, 0x1F,       # multiplex ratio = 32
            0xA4,             # display follows RAM
            0xD3, 0x00,       # display offset
            0xD5, 0x80,       # display clock divide
            0xD9, 0xF1,       # pre-charge
            0xDA, 0x02,       # COM pins
            0xDB, 0x40,       # VCOM detect
            0x8D, 0x14,       # charge pump on
            0xAF,             # display on
        )
        self.clear()
        self.show()

    def clear(self) -> None:
        self.buf[:] = b"\x00" * len(self.buf)

    def show(self) -> None:
        self._cmd(0x21, 0, self.width - 1, 0x22, 0, self.pages - 1)
        self._data(self.buf)

    # --------------------------------------------------------
    # drawing primitives
    # --------------------------------------------------------

    def pixel(self, x: int, y: int, on: bool = True) -> None:
        if not (0 <= x < self.width and 0 <= y < self.height):
            return

        i = x + (y // 8) * self.width
        bit = 1 << (y & 7)

        if on:
            self.buf[i] |= bit
        else:
            self.buf[i] &= ~bit

    def hline(self, x: int, y: int, w: int, on: bool = True) -> None:
        for xx in range(x, x + w):
            self.pixel(xx, y, on)

    def vline(self, x: int, y: int, h: int, on: bool = True) -> None:
        for yy in range(y, y + h):
            self.pixel(x, yy, on)

    def rect(self, x: int, y: int, w: int, h: int, on: bool = True, fill: bool = False) -> None:
        if w <= 0 or h <= 0:
            return

        if fill:
            for yy in range(y, y + h):
                self.hline(x, yy, w, on)
            return

        self.hline(x, y, w, on)
        self.hline(x, y + h - 1, w, on)
        self.vline(x, y, h, on)
        self.vline(x + w - 1, y, h, on)

    # --------------------------------------------------------
    # text
    # --------------------------------------------------------

    def _draw_char(self, ch: str, x: int, y: int) -> None:
        o = ord(ch)
        if o < 32 or o > 126:
            o = ord("?")

        idx = (o - 32) * 5

        for col in range(5):
            bits = FONT[idx + col]
            for row in range(7):
                if bits & (1 << row):
                    self.pixel(x + col, y + row, True)

    def text(self, s: str, x: int, y: int) -> None:
        for i, ch in enumerate(str(s)):
            self._draw_char(ch, x + i * 6, y)
