class MonoI2CDisplay(DisplayBackend):
    def __init__(self, width: int, height: int, bus: int = 1, addr: int = 0x3C):
        self.bus = smbus.SMBus(bus)
        self.addr = addr
        self.width = width
        self.height = height
        self.pages = self.height // 8
        self.buf = bytearray(self.width * self.pages)
        self.is_sleeping = False

    def _cmd(self, *cmds: int) -> None:
        data = list(cmds)
        for i in range(0, len(data), 32):
            self.bus.write_i2c_block_data(self.addr, 0x00, data[i:i + 32])

    def _data(self, data_bytes: bytes | bytearray) -> None:
        for i in range(0, len(data_bytes), 32):
            self.bus.write_i2c_block_data(self.addr, 0x40, list(data_bytes[i:i + 32]))

    def clear(self) -> None:
        self.buf[:] = b"\x00" * len(self.buf)

    def show(self) -> None:
        if self.is_sleeping:
            return
        self._cmd(0x21, 0, self.width - 1, 0x22, 0, self.pages - 1)
        self._data(self.buf)

    def set_contrast(self, value: int) -> None:
        value = max(0, min(255, int(value)))
        self._cmd(0x81, value)

    def sleep(self) -> None:
        if not self.is_sleeping:
            self._cmd(0xAE)
            self.is_sleeping = True

    def wake(self) -> None:
        if self.is_sleeping:
            self._cmd(0xAF)
            self.is_sleeping = False
            self.show()

    def pixel(self, x: int, y: int, on: bool = True) -> None:
        if not (0 <= x < self.width and 0 <= y < self.height):
            return

        i = x + (y // 8) * self.width
        bit = 1 << (y & 7)

        if on:
            self.buf[i] |= bit
        else:
            self.buf[i] &= ~bit

    def hline(self, x: int, y: int, w: int, on: bool = True) -> None:
        for xx in range(x, x + w):
            self.pixel(xx, y, on)

    def vline(self, x: int, y: int, h: int, on: bool = True) -> None:
        for yy in range(y, y + h):
            self.pixel(x, yy, on)

    def rect(self, x: int, y: int, w: int, h: int, on: bool = True, fill: bool = False) -> None:
        if w <= 0 or h <= 0:
            return

        if fill:
            for yy in range(y, y + h):
                self.hline(x, yy, w, on)
            return

        self.hline(x, y, w, on)
        self.hline(x, y + h - 1, w, on)
        self.vline(x, y, h, on)
        self.vline(x + w - 1, y, h, on)

    def _draw_char(self, ch: str, x: int, y: int, on: bool = True) -> None:
        self._draw_char_scaled(ch, x, y, 1, on=on)

    def _draw_char_scaled(self, ch: str, x: int, y: int, scale: int = 1, on: bool = True) -> None:
        o = ord(ch)
        if o < 32 or o > 126:
            o = ord("?")

        idx = (o - 32) * 5
        scale = max(1, int(scale))

        for col in range(5):
            bits = FONT[idx + col]
            for row in range(7):
                if bits & (1 << row):
                    px = x + (col * scale)
                    py = y + (row * scale)
                    for dx in range(scale):
                        for dy in range(scale):
                            self.pixel(px + dx, py + dy, on)

    def text(self, s: str, x: int, y: int, on: bool = True) -> None:
        self.text_scaled(s, x, y, 1, on=on)

    def text_scaled(self, s: str, x: int, y: int, scale: int = 1, on: bool = True) -> None:
        scale = max(1, int(scale))
        for i, ch in enumerate(str(s)):
            self._draw_char_scaled(ch, x + i * 6 * scale, y, scale, on=on)
